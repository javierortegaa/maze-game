  function drawMaze() {
    // Calculate the starting position of the maze to center it on the canvas
    var mazeStartX = (canvas.width - maze[0].length * player.width) / 2;
    var mazeStartY = (canvas.height - maze.length * player.height) / 2;

    for (var i = 0; i < maze.length; i++) {
      for (var j = 0; j < maze[i].length; j++) {
        switch (maze[i][j]) {
          case 1: // Wall
            ctx.fillStyle = "white";
            break;
          case 2: // Entrance
            ctx.fillStyle = "green";
            break;
          case 3: // Exit
            ctx.fillStyle = "blue";
            break;
          default: // Empty space
            ctx.fillStyle = "black";
            break;
        }
        var x = mazeStartX + j * player.width;
        var y = mazeStartY + i * player.height;
        ctx.fillRect(x, y, player.width, player.height);
      }
    }
  }
  // Function to draw the player
  function drawPlayer() {
    ctx.fillStyle = "red";
    ctx.fillRect(player.x, player.y, player.width, player.height);
  }

  // Function to handle player movement
  function movePlayer(dx, dy) {
    var newX = player.x + dx;
    var newY = player.y + dy;

    // Check if the new position is within the canvas boundaries and not a wall
    if (
      newX >= 0 &&
      newX + player.width <= canvas.width &&
      newY >= 0 &&
      newY + player.height <= canvas.height &&
      maze[
        Math.floor(
          (newY - (canvas.height - maze.length * player.height) / 2) /
            player.height
        )
      ][
        Math.floor(
          (newX - (canvas.width - maze[0].length * player.width) / 2) /
            player.width
        )
      ] !== 1
    ) {
      player.x = newX;
      player.y = newY;
      draw(); // Redraw the game after moving
    }
  }

  // Event listener for keydown events
  document.addEventListener("keydown", function (event) {
    // Move the player based on the pressed key
    switch (event.key) {
      case "ArrowUp":
        movePlayer(0, -player.speed);
        break;
      case "ArrowDown":
        movePlayer(0, player.speed);
        break;
      case "ArrowLeft":
        movePlayer(-player.speed, 0);
        break;
      case "ArrowRight":
        movePlayer(player.speed, 0);
        break;
    }
  });

  // Function to draw the game
  function draw() {
    ctx.clearRect(0, 0, canvas.width, canvas.height); // Clear the canvas
    drawMaze(); // Draw the maze
    drawPlayer(); // Draw the player
  }

  // Initial draw of the game
  draw();
